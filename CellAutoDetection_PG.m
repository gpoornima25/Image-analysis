% --- Executes on selection change in listbox1.
function listbox1_Callback(hObject, eventdata, handles)
% hObject    handle to listbox1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: contents = cellstr(get(hObject,'String')) returns listbox1 contents as cell array
%        contents{get(hObject,'Value')} returns selected item from listbox1
value = get(hObject,'value');
if length(value) == 0
    warndlg('No file selected','!warning');
    return
end
string = get(hObject,'string');
file_name = char(string(value));
set(handles.OpenFileName,'string',file_name);
if exist(file_name,'file')==2
else
    close(h_bar);
    return;
end
l = length(file_name);
filetype = file_name(l-2:l);
switch filetype
    case 'tif'
        Ca2dImage = imread(file_name,'tiff',1);
    case 'BLK'
%        h_bar = waitbar(0,'waiting...','name','loading data...');
        [Data DataInfo] = fileopen(file_name);
%        h_bar = waitbar(0.5);
        Ca2dImage = mean(Data(:,:,1:50),3);
end

axes(handles.Image1);
set(0,'DefaultImageCreateFcn','axis image'); % makes each pixel square; essential for accurate aspect ratio
imagesc(Ca2dImage);
colormap('gray');
handles.Ca2dImage=Ca2dImage;
close(h_bar);
guidata(hObject,handles);


